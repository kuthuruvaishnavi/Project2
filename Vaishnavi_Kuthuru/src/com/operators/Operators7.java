package com.operators;

//Shift Operators 
public class Operators7 {

	public static void main(String[] args) {
		System.out.println("Main method Started!");
		
		int a = 8; //bit to move
		int b = 2;// how many bits to move
		
		
		System.out.println(a << b);
		System.out.println(a >> b);
		System.out.println(a >>> b);

	}

}
